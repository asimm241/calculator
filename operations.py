"""arithmetic operations functions"""

def add(op1, op2):
    """Add given params"""
    return op1 + op2


def minus(op1, op2):
    """Subtract given params"""
    return op1 - op2


def multiply(op1, op2):
    """Multiply given params"""
    return op1 * op2


def divide(op1, op2):
    """divide given params"""
    return op1 / op2